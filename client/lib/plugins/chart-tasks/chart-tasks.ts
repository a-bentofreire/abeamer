  /**
   * Returns the maximum value of array of array of numbers.
   */
  function _maxOfArrayArray(data: number[][], startValue: number): number {
    data.forEach(series => {
      series.forEach(point => {
        startValue = Math.max(startValue, point);
      });
    });
    return startValue;
  }

